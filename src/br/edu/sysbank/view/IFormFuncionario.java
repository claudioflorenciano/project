    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        Endereco e = new Endereco();
        
        e.setCEP(jFormattedTextFieldCEP.getText());
        e.setBairro(jTextFieldBairro.getText());
        e.setLogradouro(jTextFieldLogradouro.getText());
        e.setCidade(jTextFieldCidade.getText());
        e.setUF(jTextFieldUF.getText());
        
        funcionario = new Funcionario(e);
        Cargo cargo = new Cargo();

        funcionario.setNome(jTextFieldNome.getText());
        funcionario.setEmail(jTextFieldEmail.getText());
        funcionario.setCpf(jFormattedTextFieldCPF.getText());
        funcionario.setNumero(jTextFieldNumero.getText());
        funcionario.setComplemento(jTextFieldComplemento.getText());
        funcionario.setPws(new String(jPasswordFieldSenha1.getPassword()));
        cargo.setIdCargo(cargos.get(jComboBoxCargo.getSelectedIndex()).getIdCargo());
        cargo.setNomeCargo(cargos.get(jComboBoxCargo.getSelectedIndex()).getNomeCargo());
        funcionario.setCargo(cargo);
        //System.out.println(jPasswordFieldSenha1.getPassword().toString());

        
        ctrlFuncionario = new CtrlFuncionario();
        String erros = "";
        if (this.getTitle().equals("Atualizar Funcionario")) {
            erros = ctrlFuncionario.valida(funcionario);
            if (erros.equals("")) {
                try {
                    if (ctrlFuncionario.atualizar(funcionario)) {
                        JOptionPane.showMessageDialog(null, "Atuallizado", "Aviso", 1);
                        
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Erro ao atualizar no Sistema.", "ERRO", 0);
                }
            } else {
                JOptionPane.showMessageDialog(null, erros, "Erro(s) ao cadastrar", 0);
            }
        } else {
            erros = ctrlFuncionario.valida(funcionario, new String(jPasswordFieldSenha2.getPassword()));
            if (erros.equals("")) {
                if (ctrlFuncionario.salvar(funcionario)) {
                    JOptionPane.showMessageDialog(null, "Cadastrado", "Aviso", 1);
                    limparCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao Cadastrar no Sistema", "ERRO", 0);
                }
            } else {
                JOptionPane.showMessageDialog(null, erros, "Erro(s) ao cadastrar", 0);
            }
        }
        jButtonCancelar.doClick();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

