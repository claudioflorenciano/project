    public void mostraCargos(){
        try{
            ctrlFuncionario = new CtrlFuncionario();
            cargos = ctrlFuncionario.listarCargos();

            modeloCombo.removeAllElements();
            jComboBoxCargo.setModel(modeloCombo);

            if(!cargos.isEmpty()){
                for(Cargo c : cargos){
                    jComboBoxCargo.addItem(c.getNomeCargo());
                    if(this.getTitle().equals("Atualizar Funcionario")){
                        if(c.getNomeCargo().equals(funcionario.getCargo().getNomeCargo())){
                            jComboBoxCargo.setSelectedItem(c.getNomeCargo());
                        }
                    }
                }
            }
        }catch(Exception erro){
            System.err.println("Erro ao pesquisar cargos: " + erro);
        }
    }
    
    public void preparaCampos(long id){
        this.setTitle("Atualizar Funcionario");
        jPasswordFieldSenha1.setVisible(false);
        jPasswordFieldSenha2.setVisible(false);
        jLabelSenha1.setVisible(false);
        jLabelSenha2.setVisible(false);

        jFormattedTextFieldCPF.setEnabled(false);
        jTextFieldLogradouro.setEnabled(false);
        jTextFieldBairro.setEnabled(false);
        jTextFieldCidade.setEnabled(false);
        jTextFieldUF.setEnabled(false);

        ctrlFuncionario = new CtrlFuncionario();
        funcionario = ctrlFuncionario.getFuncionario(id);

        jTextFieldNome.setText(funcionario.getNome());
        jFormattedTextFieldCPF.setText(funcionario.getCpf());
        jTextFieldEmail.setText(funcionario.getEmail());
        jTextFieldNumero.setText(funcionario.getNumero());
        jTextFieldComplemento.setText(funcionario.getComplemento());
        jFormattedTextFieldCEP.setText(funcionario.getEndereco().getCEP());
        jTextFieldLogradouro.setText(funcionario.getEndereco().getLogradouro());
        jTextFieldBairro.setText(funcionario.getEndereco().getBairro());
        jTextFieldCidade.setText(funcionario.getEndereco().getCidade());
        jTextFieldUF.setText(funcionario.getEndereco().getUF());
        mostraCargos();
        
    }
    
