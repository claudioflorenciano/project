    private void prepararCampos(long id) {
        this.setTitle("Atualizar Cliente");
        jPasswordFieldSenha1.setVisible(false);
        jPasswordFieldSenha2.setVisible(false);
        jLabelSenha.setVisible(false);
        jLabelSenha2.setVisible(false);

        jFormattedTextFieldCPF.setEnabled(false);
        jTextFieldLogradouro.setEnabled(false);
        jTextFieldBairro.setEnabled(false);
        jTextFieldCidade.setEnabled(false);
        jTextFieldUF.setEnabled(false);

        ctrlCliente = new CtrlCliente();
        cliente = ctrlCliente.getCliente(id);

        jTextFieldNome.setText(cliente.getNome());
        jFormattedTextFieldCPF.setText(cliente.getCpf());
        jTextFieldEmail.setText(cliente.getEmail());
        jTextFieldNumero.setText(cliente.getNumero());
        jTextFieldComplemento.setText(cliente.getComplemento());
        jFormattedTextFieldCEP.setText(cliente.getEndereco().getCEP());
        jTextFieldLogradouro.setText(cliente.getEndereco().getLogradouro());
        jTextFieldBairro.setText(cliente.getEndereco().getBairro());
        jTextFieldCidade.setText(cliente.getEndereco().getCidade());
        jTextFieldUF.setText(cliente.getEndereco().getUF());
        if (cliente.isAtivo()) {
            jRadioButtonAtivo.isSelected();
        } else {
            jRadioButtonBloqueado.isSelected();
        }
    }
    
