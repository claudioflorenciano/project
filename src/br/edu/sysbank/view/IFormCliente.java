    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        Endereco e = new Endereco();
        e.setCEP(jFormattedTextFieldCEP.getText());
        e.setBairro(jTextFieldBairro.getText());
        e.setLogradouro(jTextFieldLogradouro.getText());
        e.setCidade(jTextFieldCidade.getText());
        e.setUF(jTextFieldUF.getText());

        cliente = new Cliente(e);

        cliente.setNome(jTextFieldNome.getText());
        cliente.setEmail(jTextFieldEmail.getText());
        cliente.setCpf(jFormattedTextFieldCPF.getText());
        cliente.setNumero(jTextFieldNumero.getText());
        cliente.setComplemento(jTextFieldComplemento.getText());
        cliente.setPws(new String(jPasswordFieldSenha1.getPassword()));
        //System.out.println(jPasswordFieldSenha1.getPassword().toString());
        cliente.setDataCadastro(new Date());

        ctrlCliente = new CtrlCliente();
        String erros = "";
        //Teste o titulo para aplicar a atualização
        if (this.getTitle().equals("Atualizar Cliente")) {
            erros = ctrlCliente.valida(cliente);
            if (erros.equals("")) {
                try {
                    if (ctrlCliente.atualizar(cliente)) {
                        JOptionPane.showMessageDialog(null, "Atuallizado", "Aviso", 1);
                        
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Erro ao atualizar no Sistema.", "ERRO", 0);
                }
            } else {
                JOptionPane.showMessageDialog(null, erros, "Erro(s) ao cadastrar", 0);
            }
        } else {
            erros = ctrlCliente.valida(cliente, new String(jPasswordFieldSenha2.getPassword()));
            if (erros.equals("")) {
                if (ctrlCliente.salvar(cliente)) {
                    JOptionPane.showMessageDialog(null, "Cadastrado", "Aviso", 1);
                    limparCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao Cadastrar no Sistema", "ERRO", 0);
                }
            } else {
                JOptionPane.showMessageDialog(null, erros, "Erro(s) ao cadastrar", 0);
            }
        }
        jButtonCancelar.doClick();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

