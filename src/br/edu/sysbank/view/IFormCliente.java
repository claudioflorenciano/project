    private void jButtonBuscaCEPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscaCEPActionPerformed
        // TODO add your handling code here:
        CtrlEndereco ctrlEndereco = new CtrlEndereco();
        Endereco endereco = ctrlEndereco.pesquisar(jFormattedTextFieldCEP.getText());
        
        if(endereco == null){
            JOptionPane.showMessageDialog(null, "CEP não localizado.");
        }else{
            jTextFieldBairro.setText(endereco.getBairro());
            jTextFieldCidade.setText(endereco.getCidade());
            jTextFieldLogradouro.setText(endereco.getLogradouro());
            jTextFieldUF.setText(endereco.getUF());
        }
    }//GEN-LAST:event_jButtonBuscaCEPActionPerformed

